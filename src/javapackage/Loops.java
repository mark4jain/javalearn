package javapackage;

public class Loops {

	public static void main(String[] args) {
		//1. while loop
		
		int i =0;
		while(i<=10) {
			System.out.println(i);
			i++;
			//++i;
			//i= i+1;
		}
		System.out.println("================");
		
		//2. for loop
		// 1 to 10
		
		for(int j=1; j<=10; j++) {
			System.out.println(j);

		}
		
		
		
	}

}
